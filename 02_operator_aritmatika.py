# Tujuan: Menggunakan operator matematika dasar (*) untuk melakukan perhitungan.

# --- Kode Anda Dimulai Di Sini ---

# Membuat variabel untuk data belanja
harga_buku = 15000
jumlah_buku = 3

# Melakukan proses kalkulasi
total_harga = harga_buku * jumlah_buku

# Menampilkan hasil akhir dalam format struk sederhana
print("--- Struk Belanja Sederhana ---")
print("Harga satuan buku: Rp", harga_buku)
print("Jumlah buku yang dibeli:", jumlah_buku)
print("Total yang harus dibayar adalah: Rp", total_harga)

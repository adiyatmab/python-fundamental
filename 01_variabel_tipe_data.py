# Latihan 1: Variabel dan Tipe Data
# Tujuan: Memahami cara membuat variabel dengan tipe data yang berbeda (String, Integer, Boolean).

# --- Kode Anda Dimulai Di Sini ---

# Membuat variabel untuk merepresentasikan seekor hewan
nama_hewan = "Anjing"       # Tipe data String (teks)
jumlah_kaki = 4             # Tipe data Integer (angka bulat)
apakah_mamalia = True       # Tipe data Boolean (Benar/Salah) -> Ingat, 'True' bukan 'true'

# Menampilkan hasil ke layar dengan format yang rapi
print("--- Informasi Hewan ---")
print("Nama Hewan: ", nama_hewan)
print("Jumlah Kaki: ", jumlah_kaki)
print("Termasuk Mamalia: ", apakah_mamalia)
